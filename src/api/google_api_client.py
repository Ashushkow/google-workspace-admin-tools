    def get_groups(self, max_results: int = None) -> List[Dict[str, Any]]:
        """
        –ü–æ–ª—É—á–∞–µ—Ç —Å–ø–∏—Å–æ–∫ –≤—Å–µ—Ö –≥—Ä—É–ø–ø —Å –ø–∞–≥–∏–Ω–∞—Ü–∏–µ–π
        
        Args:
            max_results: –ú–∞–∫—Å–∏–º–∞–ª—å–Ω–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–æ–≤ (None = –≤—Å–µ)
            
        Returns:
            –°–ø–∏—Å–æ–∫ –≥—Ä—É–ø–ø
        """
        try:
            if not self.service:
                return []
            
            all_groups = []
            page_token = None
            
            while True:
                request_params = {
                    'customer': 'my_customer',
                    'maxResults': 200  # –ò—Å–ø–æ–ª—å–∑—É–µ–º –º–∞–∫—Å–∏–º–∞–ª—å–Ω—ã–π —Ä–∞–∑–º–µ—Ä —Å—Ç—Ä–∞–Ω–∏—Ü—ã
                }
                
                if page_token:
                    request_params['pageToken'] = page_token
                
                result = self.service.groups().list(**request_params).execute()
                groups = result.get('groups', [])
                
                if groups:
                    all_groups.extend(groups)
                    logger.debug(f"–ó–∞–≥—Ä—É–∂–µ–Ω–∞ —Å—Ç—Ä–∞–Ω–∏—Ü–∞ –≥—Ä—É–ø–ø: {len(groups)} –∑–∞–ø–∏—Å–µ–π")
                
                # –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ —Å–ª–µ–¥—É—é—â–µ–π —Å—Ç—Ä–∞–Ω–∏—Ü—ã
                page_token = result.get('nextPageToken')
                if not page_token:
                    break
                    
                # –ï—Å–ª–∏ –∑–∞–¥–∞–Ω–æ –æ–≥—Ä–∞–Ω–∏—á–µ–Ω–∏–µ –∏ –º—ã –µ–≥–æ –¥–æ—Å—Ç–∏–≥–ª–∏
                if max_results and len(all_groups) >= max_results:
                    all_groups = all_groups[:max_results]
                    break
            
            logger.info(f"–ó–∞–≥—Ä—É–∂–µ–Ω–æ –≥—Ä—É–ø–ø: {len(all_groups)}")
            return all_groups
            
        except Exception as e:
            logger.error(f"–û—à–∏–±–∫–∞ –ø–æ–ª—É—á–µ–Ω–∏—è –≥—Ä—É–ø–ø: {e}")
            return []
    
